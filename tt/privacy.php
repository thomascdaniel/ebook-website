Disclaimer: This summary is not a part of the Privacy Policy and is not a legal document. It is simply a handy reference for understanding the full Privacy Policy. Think of it as the user-friendly interface to our Privacy Policy.
Because we believe that you shouldn’t have to provide personal information to participate in the free knowledge movement, you may:

Read, edit, or use any website without registering an account.
Register for an account without providing an email address or real name.
Because we want to understand how websites are used so we can make them better for you, we collect some information when you:

Make public contributions.
Register an account or update your user page.
Use the Websites Sites.
Send us emails or participate in a survey or give feedback.
We are committed to:

Describing how your information may be used or shared in this Privacy Policy.
Using reasonable measures to keep your information secure.
Never selling your information or sharing it with third parties for marketing purposes.
Only sharing your information in limited circumstances, such as to improve the Websites Sites, to comply with the law, or to protect you and others.
Retaining your data for the shortest possible time that is consistent with maintaining, understanding, and improving the Websites Sites, and our obligations under law.
Be aware:

Any content you add or any change that you make to a Websites Site will be publicly and permanently available.
If you add content or make a change to a Websites Site without logging in, that content or change will be publicly and permanently attributed to the IP address used at the time rather than a username. 
Our community of volunteer editors and contributors is a self-policing body. Certain administrators of the Websites Sites, who are chosen by the community, use tools that grant them limited access to nonpublic information about recent contributions so they may protect the Websites Sites and enforce policies. 
This Privacy Policy does not apply to all sites and services run by the Websites Foundation, such as sites or services that have their own privacy policy (like the Websites Shop) or sites or services run by third parties (like third-party developer projects on Websites Labs).
As part of our commitment to education and research around the world, we occasionally release public information and aggregated or non-personal information to the general public through data dumps and data sets.
For the protection of the Websites Foundation and other users, if you do not agree with this Privacy Policy, you may not use the Websites Sites.
